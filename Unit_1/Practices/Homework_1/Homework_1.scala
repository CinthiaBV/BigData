Interpretation of the Karl Pearson correlation coefficient

Pearson's correlation coefficient has the objective of indicating how associated two variables are with each other, therefore:
Correlation less than zero: If the correlation is less than zero, it means that it is negative, that is, the variables are inversely related.
When the value of some variable is high, the value of the other variable is low. The closer it is to -1, the clearer the extreme covariance. 
If the coefficient is equal to -1, we mean a perfect negative correlation.


Correlation greater than zero: If the correlation is equal to +1 it means that it is perfect positive. In this case it means that the correlation
 is positive, that is, that the variables are directly correlated.
When the value of one variable is high, the value of the other is also high, the same happens when they are low. If it is close to +1, the coefficient
 will be the covariance.
Correlation equal to zero: When the correlation is equal to zero it means that it is not possible to determine some sense of covariation. However,
 it does not mean that there is no nonlinear relationship between the variables.
When the variables are independent, it means that they are correlated, but this does not mean that the result is true.
Advantages and disadvantages of the Pearson correlation coefficient

Among the main advantages of the Karl Pearson correlation coefficient are:
• The value is independent of whatever unit is used to measure the variables.
• If the sample is large, the accuracy of the estimate is more likely.
Some of the disadvantages of the correlation coefficient are:
• It is necessary both variables be measured at a continuous quantitative level.
• The distribution of the variables must be similar to the normal curve.



